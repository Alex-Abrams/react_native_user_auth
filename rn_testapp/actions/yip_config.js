function generateHashtag (str) {
  let string = "";
  for(let i = 0; i < str.length; i ++) {
    if (str[i] != "_") {
      string += str[i];
    }
  }
  let x = string.slice(1, string.length);
  let z = string.slice(0, 1).to_UpperCase();
  return "#" + z + x;
}
